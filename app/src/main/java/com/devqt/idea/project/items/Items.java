package com.devqt.idea.project.items;


public class Items {

    private String nameApp;
    private String iconApp;
    private String description;
    private String videoApp;

    public String getName() {
        return nameApp;
    }
    public void setName(String name) {
        this.nameApp = name;
    }
    public String geticonApp() {
        return iconApp;
    }
    public void seticonApp(String iconApp) {
        this.iconApp = iconApp;
    }
    public String getdescription() {
        return description;
    }
    public void setdescription(String description) {
        this.description = description;
    }
    public String getvideoApp() {
        return videoApp;
    }
    public void setvideoApp(String videoApp) {
        this.videoApp = videoApp;
    }

}